 #### Estrutura da notificação

 O conteúdo da notificação com o tipo **UNMutableNotificationContent**

 Contém as seguintes propriedades:
 1. *title*: cabeçalho da notificação
 2. *subtitle*: descrição da notificação
 3. *body*: Corpo com o texto principal da notificação
 4. *badge*: Número a ser exibido no ícone do aplicativo por notificação
 5. *sound*: Som emitido pela notificação. Do tipo *UNNotificationSound*. Para usar o som padrão, use *UNNotificationSound.default()*
 6. *launchImageName*: Nome do arquivo de imagem do ícone. Utilize `nil` para usar a imagem padrão do aplicativo
 7. *userInfo*: Dicionário usado para armazenar informações do usuário que podem ser enviadas para uma notificação.
 8. *attachments*: Utilizado para ativar áudio, imagem ou vídeo

 */

